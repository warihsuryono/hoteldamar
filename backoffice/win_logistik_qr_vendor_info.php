		if (strval($this->supplier->CurrentValue) <> "") {
			$sSqlWrk = "SELECT `nama`, `alamat` FROM `mst_vendor` WHERE `kode` = '" . ew_AdjustSql($this->supplier->CurrentValue) . "'";
			$sSqlWrk .= " ORDER BY `nama` ";
			$rswrk = $conn->Execute($sSqlWrk);
			if ($rswrk && !$rswrk->EOF) { // Lookup value(s) found
				$this->supplier->ViewValue = $rswrk->fields('nama');
				$this->supplier->ViewValue .= ew_ValueSeparator(0) . $rswrk->fields('alamat');
				$rswrk->Close();
			} else {
				$this->supplier->ViewValue = $this->supplier->CurrentValue;
			}
		} else {
			$this->supplier->ViewValue = NULL;
		}
		$this->supplier->CssStyle = "";
		$this->supplier->CssClass = "";
		$this->supplier->ViewCustomAttributes = "";

		// tanggal
		$this->tanggal->ViewValue = $this->tanggal->CurrentValue;
		$this->tanggal->ViewValue = ew_FormatDateTime($this->tanggal->ViewValue, 7);
		$this->tanggal->CssStyle = "";
		$this->tanggal->CssClass = "";
		$this->tanggal->ViewCustomAttributes = "";

		// receiveby
		if (strval($this->receiveby->CurrentValue) <> "") {
			$sSqlWrk = "SELECT `nama` FROM `user_account` WHERE `username` = '" . ew_AdjustSql($this->receiveby->CurrentValue) . "'";
			$sSqlWrk .= " ORDER BY `nama` ";
			$rswrk = $conn->Execute($sSqlWrk);
			if ($rswrk && !$rswrk->EOF) { // Lookup value(s) found
				$this->receiveby->ViewValue = $rswrk->fields('nama');
				$rswrk->Close();
			} else {
				$this->receiveby->ViewValue = $this->receiveby->CurrentValue;
			}
		} else {
			$this->receiveby->ViewValue = NULL;
		}
		$this->receiveby->CssStyle = "";
		$this->receiveby->CssClass = "";
		$this->receiveby->ViewCustomAttributes = "";

		// qrno
		$this->qrno->HrefValue = "";

		// vendorid
		$this->vendorid->HrefValue = "";

		// supplier
		$this->supplier->HrefValue = "";

		// tanggal
		$this->tanggal->HrefValue = "";

		// receiveby
		$this->receiveby->HrefValue = "";

		// Call Row Rendered event
		$this->Row_Rendered();
