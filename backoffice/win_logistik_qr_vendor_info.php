	function RenderListRow() {
		global $conn, $Security;

		// Call Row Rendering event
		$this->Row_Rendering();

		// qrno
		$this->qrno->ViewValue = $this->qrno->CurrentValue;
		$this->qrno->CssStyle = "";
		$this->qrno->CssClass = "";
		$this->qrno->ViewCustomAttributes = "";

